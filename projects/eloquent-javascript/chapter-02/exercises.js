function drawChessboard(num) {
  var chessBoard = '';
  for(var i = 0; i < num; i++) {
    for(var j = 0; j < num; j++) {
      if((i + j) % 2 === 0) {
        chessBoard += ' ';
      } else {
        chessBoard += '#'
      } 
  } chessBoard += '\n'
} console.log(chessBoard);
}

