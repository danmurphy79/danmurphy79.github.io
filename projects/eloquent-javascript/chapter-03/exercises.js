function countChars(string, char) {
//takes a string and a char as  arguments
  let arr = [];
  for(let i = 0; i < string.length; i++) {//loop through the string
    if(string[i] === char) {
      arr.push(string[i]);//return the number of char in the string
    }
  } return arr.length;
}


