function countBs(string) {
//takes a strng as its only argument
  let arr = [];
  for(let i = 0; i < string.length; i++) {//loop through the string
    if(string[i] === 'B') {
      arr.push(string[i]);//return the number of capital Bs in the string
    }
  } return arr.length;
}

